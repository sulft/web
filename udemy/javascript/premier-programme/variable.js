/* Les Variables */

// 2 types de variables
// var / let
//var -> agit comme variable globale
//let -> agit comme variable locale

/* Utilisons "let" */
// déclaration d'une valeur String sois entre guillemet ou apostrophe
//Chaines de caractères ou String.
let prenom = "Marc"; // déclarer une variable
prenom = 'John'; // modifier la variable

//Nombre
let age = 15;
age = 21.5;

//boolean
let majeur = true;
majeur = false

