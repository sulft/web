import java.util.*;

public class JavaStdin {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        int a = sc.nextInt();
        int b = sc.nextInt();
        int c = sc.nextInt();

        System.out.println("---");

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);

    }
}